#include <stdio.h>

/**
 * add - Sum of two int
 * @a: number to sum
 * @b: number to sum
 *
 * Return: Sum of two number
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - difference between two number
 * @a: number
 * @b: number
 *
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication
 * @a: number
 * @b: number
 *
 * Return: result
 */
int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - division
 * @a: number
 * @b: number
 *
 * Return: result
 */
int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - difference between two number
 * @a: number
 * @b: number
 *
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
